SELECT A.ANIMAL_ID, A.ANIMAL_TYPE, A.NAME
FROM ANIMAL_INS AS A JOIN ANIMAL_OUTS AS B ON A.ANIMAL_ID = B.ANIMAL_ID
WHERE (SEX_UPON_INTAKE = 'Intact Male' AND SEX_UPON_OUTCOME = 'Neutered Male')
OR (SEX_UPON_INTAKE = 'Intact Female' AND SEX_UPON_OUTCOME = 'Spayed Female')
ORDER BY ANIMAL_ID ASC;